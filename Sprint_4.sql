
CREATE TABLE Cliente (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    cpf VARCHAR2(11) NOT NULL,
    nome VARCHAR2(255) NOT NULL,
    email VARCHAR2(255) NOT NULL,
    algoritmo VARCHAR2(255) NOT NULL,
    modelo VARCHAR2(255) NOT NULL
);


CREATE TABLE Empresa (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    cnpj VARCHAR2(18) NOT NULL,
    nome VARCHAR2(255) NOT NULL,
    endereco VARCHAR2(255) NOT NULL,
    email VARCHAR2(255) NOT NULL,
    cliente_id NUMBER,
    CONSTRAINT fk_empresa_cliente FOREIGN KEY (cliente_id) REFERENCES Cliente(id)
);


CREATE TABLE Venda (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    valor VARCHAR2(50) NOT NULL,
    descvenda VARCHAR2(255) NOT NULL,
    cliente_id NUMBER,
    CONSTRAINT fk_venda_cliente FOREIGN KEY (cliente_id) REFERENCES Cliente(id)
);


CREATE TABLE Oferta (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    desconto VARCHAR2(50) NOT NULL,
    porcentagem VARCHAR2(50) NOT NULL,
    venda_id NUMBER,
    CONSTRAINT fk_oferta_venda FOREIGN KEY (venda_id) REFERENCES Venda(id)
);


DROP SEQUENCE cliente_seq;
DROP SEQUENCE empresa_seq;
DROP SEQUENCE oferta_seq;
DROP SEQUENCE venda_seq;


CREATE SEQUENCE cliente_seq START WITH 1 INCREMENT BY 1;
CREATE SEQUENCE empresa_seq START WITH 1 INCREMENT BY 1;
CREATE SEQUENCE oferta_seq START WITH 1 INCREMENT BY 1;
CREATE SEQUENCE venda_seq START WITH 1 INCREMENT BY 1;
